class DrawingPainter extends CustomPainter {
  final List<double> xCoordinates;
  final List<double> yCoordinates;

  DrawingPainter(this.xCoordinates, this.yCoordinates);

  @override
  void paint(Canvas canvas, Size size) {
    final paint = Paint()
      ..color = Colors.blue
      ..strokeWidth = 2
      ..strokeCap = StrokeCap.round;

    for (int i = 0; i < xCoordinates.length - 1; i++) {
      canvas.drawLine(
        Offset(xCoordinates[i], yCoordinates[i]),
        Offset(xCoordinates[i + 1], yCoordinates[i + 1]),
        paint,
      );
    }
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) {
    return true;
  }
}
